function Search({ query, setQuery }) {
  const inputEl = useRef(null);
  const { current } = inputEl;
  useKey("Enter", () => {
    if (document.activeElement !== current) {
      current.focus();
      setQuery("");
    }
  });

  return (
    <input
      className="search"
      type="text"
      placeholder="Search movies..."
      value={query}
      onChange={(e) => setQuery(e.target.value)}
      ref={inputEl}
    />
  );
}

