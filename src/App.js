function MovieDetails({
  selectedId,
  onCloseMovie,
  onAddWatched,
  exists,
  onRemove,
}) {
  const [movie, setMovie] = useState({});
  const [isLoading, setIsLoading] = useState(false);
  const {
    Title: title,
    Year: year,
    Poster: poster,
    Runtime: runtime,
    imdbRating,
    Plot: plot,
    Released: released,
    Actors: actors,
    Director: director,
    Genre: genre,
  } = movie;

  useEffect(() => {
    document.title = `Movie | ${title}`;
    return () => (document.title = "usePopcorn");
  }, [title]);

  function handleAdd() {
    const newWatchedMovie = {
      imdbID: selectedId,
      title,
      year,
      poster,
      imdbRating: Number(imdbRating),
      runtime: Number(runtime.split(" ").at(0)),
      userRating,
    };
    onAddWatched(newWatchedMovie);
    onCloseMovie();
  }

  useKey("Escape", onCloseMovie);

  useEffect(() => {
    async function getMovie() {
      setIsLoading(true);
      const res = await fetch(
        `http://www.omdbapi.com/?apikey=f08f0bf1&i=${selectedId}`
      );
      const data = await res.json();
      setMovie(data);
      setIsLoading(false);
    }
    getMovie();
  }, [selectedId]);
  const [userRating, setUserRating] = useState(exists?.userRating);
  return (
    <div className="details">
      {isLoading ? (
        <Loader />
      ) : (
        <>
          <header>
            <button className="btn-back" onClick={onCloseMovie}>
              &larr;
            </button>
            <img src={poster} alt={`Poster of ${title} movie`} />
            <div className="details-overview">
              <h1>{title}</h1>
              <p>
                {released} &bull; {runtime}
              </p>
              <p>{genre}</p>
              <p>
                <span>‚≠êÔ∏è</span>
                {imdbRating} IMDb rating
              </p>
            </div>
          </header>
          <section>
            <div className="rating">
              {exists ? (
                <p>You rated with movie {userRating} ‚≠êÔ∏è</p>
              ) : (
                <>
                  <StarRating
                    maxRating={10}
                    size={24}
                    onSetRating={setUserRating}
                  />
                  {userRating && (
                    <button className="btn-add" onClick={handleAdd}>
                      + Add to list
                    </button>
                  )}
                </>
              )}
            </div>
            <p>
              <em>{plot}</em>
            </p>
            <p>Starring {actors}</p>
            <p>Directed by {director}</p>
          </section>
        </>
      )}
    </div>
  );
}

export default function App() {
  const [query, setQuery] = useState("");
  const [selectedId, setSelectedId] = useState(null);
  const { movies, isLoading, error } = useMovies(query, handleClose);
  const [watched, setWatched] = useLocalStorageState("watched", []);

  function handleSelect(id) {
    if (selectedId === id) {
      handleClose();
      return;
    }
    setSelectedId(id);
  }

  function handleClose() {
    setSelectedId(null);
  }

  function addWatched(movie) {
    setWatched((watched) => [...watched, movie]);
  }

  function removeWatched(id) {
    setWatched(watched.filter((m) => m.imdbID !== id));
  }

  return (
    <>
      <NavBar>
        <Search query={query} setQuery={setQuery} />
        <Results movies={movies} />
      </NavBar>
      <Main>
        {/* <Box>
          <Movies movies={movies} />
        </Box> */}
        <Box>
          {isLoading && <Loader />}
          {error && <ErrorHandler error={error} />}
          {!isLoading && !error && (
            <Movies onSelectMovie={handleSelect} movies={movies} />
          )}
        </Box>
        <Box>
          {selectedId ? (
            <MovieDetails
              key={selectedId}
              onCloseMovie={handleClose}
              selectedId={selectedId}
              onAddWatched={addWatched}
              exists={watched.find((m) => m.imdbID === selectedId)}
            />
          ) : (
            <>
              <MovieSummary watched={watched} />
              <WatchedMovie onRemove={removeWatched} watched={watched} />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}
